        public static bool ValidarEmail(string emailTx)
        {
            
            Regex email = new Regex(@"^[A-Za-z0-9](([_\.\-]?[a-zA-Z0-9]+)*)@([A-Za-z0-9]+)(([\.\-]?[a-zA-Z0-9]+)*)\.([A-Za-z]{2,})$");

            if (!email.IsMatch(emailTx))
            {
                return false;
            }

            return true;

        }

